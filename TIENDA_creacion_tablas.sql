--- CREACION DE TABLAS
CREATE TABLE TIPO_PRODUCTO
(id_tipo NUMBER(10) NOT NULL
, descripcion VARCHAR(100) NOT NULL
, CONSTRAINT TIPO_PRODUCTO_PK PRIMARY KEY(id_tipo));

CREATE TABLE PRODUCTO
(cod_producto NUMBER(10) NOT NULL
, descripcion VARCHAR(100) NOT NULL
, precio_unitario NUMBER(10) NOT NULL
, id_tipo NUMBER(10) NOT NULL
, CONSTRAINT PRODUCTO_PK PRIMARY KEY(cod_producto)
, CONSTRAINT PRODUCTO_TIPO_PRODUCTO_FK FOREIGN KEY(id_tipo) REFERENCES TIPO_PRODUCTO(id_tipo));
