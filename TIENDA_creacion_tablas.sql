CREATE TABLE BOLETA
(nro_boleta NUMBER(10) NOT NULL
,fecha DATE NOT NULL
,total NUMBER(10) NOT NULL
,CONSTRAINT BOLETA_PK PRIMARY KEY(nro_boleta));

CREATE TABLE DET_BOLETA
(nro_boleta NUMBER(10) NOT NULL
,cod_producto NUMBER(10) NOT NULL
,cantidad NUMBER(10) NOT NULL
,subtotal NUMBER(10) NOT NULL
,CONSTRAINT DET_BOLETA_PK PRIMARY KEY(nro_boleta,cod_producto)
,CONSTRAINT DET_BOLETA_BOLETA_FK FOREIGN KEY(nro_boleta) REFERENCES BOLETA(nro_boleta)
,CONSTRAINT DET_BOLETA_PRODUCTO_FK FOREIGN KEY(cod_producto) REFERENCES PRODUCTO(cod_producto));



